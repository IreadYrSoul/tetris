
/**
 * Contains configuration app properties.
 */
object Configuration {

    /**
     *  the Display height.
     */
    val displayHeight = 400

    /**
     * the Display width.
     */
    val displeyWidth = 200

    /**
     * the Node width/height.
     */
    val nodeSize = 20

    /**
     * the number Nodes of horizontally.
     */
    val height = displayHeight / nodeSize

    /**
     * the number Nodes of vertically.
     */
    val width = displeyWidth / nodeSize
}